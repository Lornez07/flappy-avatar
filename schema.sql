-- View: Top 100 scores (for easier queries)
CREATE VIEW leaderboard_top_100 AS
SELECT
  ROW_NUMBER() OVER (ORDER BY score DESC) as rank,
  player_name,
  score,
  avatar_url,
  created_at
FROM leaderboards
ORDER BY score DESC
LIMIT 100;

-- View: Daily leaderboard (scores from today)
CREATE VIEW leaderboard_daily AS
SELECT
  ROW_NUMBER() OVER (ORDER BY score DESC) as rank,
  player_name,
  score,
  avatar_url,
  created_at
FROM leaderboards
WHERE DATE(created_at) = CURRENT_DATE
ORDER BY score DESC;

