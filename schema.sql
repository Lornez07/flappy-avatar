-- ============================================================================
-- LEADERBOARD TABLE
-- ============================================================================

CREATE TABLE leaderboards (
  id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
  player_name TEXT NOT NULL,
  score INT NOT NULL DEFAULT 0,
  avatar_url TEXT,
  CONSTRAINT valid_score CHECK (score >= 0 AND score <= 999999),
  CONSTRAINT valid_name_length CHECK (char_length(player_name) >= 1 AND char_length(player_name) <= 50)
);

-- Indexes for performance
CREATE INDEX idx_leaderboards_score ON leaderboards(score DESC);
CREATE INDEX idx_leaderboards_player_name ON leaderboards(LOWER(player_name));
CREATE INDEX idx_leaderboards_created_at ON leaderboards(created_at DESC);

-- ============================================================================
-- ROW LEVEL SECURITY (RLS)
-- ============================================================================

-- Enable RLS on table
ALTER TABLE leaderboards ENABLE ROW LEVEL SECURITY;

-- Allow anyone to read leaderboard
CREATE POLICY "leaderboards_read_all" ON leaderboards
  FOR SELECT USING (true);

-- Allow anonymous inserts (score submission)
-- In production, you may want to add rate limiting or require auth
CREATE POLICY "leaderboards_insert_anonymous" ON leaderboards
  FOR INSERT WITH CHECK (true);

